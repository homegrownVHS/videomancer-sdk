"""
Generate a VHDL-2008 testbench (tb_vit.vhd) that:
  1. Reads a pre-generated stimulus.txt pixel/sync stream
  2. Drives the program_top DUT clock-by-clock
  3. Captures output pixels using the DUT's own output hsync/vsync timing
  4. Stops automatically when stimulus is exhausted

Pipeline delay compensation strategy:
  Instead of guessing or parsing the pipeline delay, the testbench captures
  output pixels aligned with the DUT's OWN output sync signals
  (data_out.hsync_n / data_out.vsync_n).  Every program delays its sync
  signals by C_PROCESSING_DELAY_CLKS to align with the processed data, so
  using the output sync timing naturally produces correctly-aligned capture
  — matching what the real hardware video encoder would see.
"""

from __future__ import annotations

from pathlib import Path

from .config import ABI_SPI_RAM_SIZE, SIM_CLK_PERIOD_NS, SIM_WARMUP_FRAMES


# ---------------------------------------------------------------------------
# VHDL testbench template
# ---------------------------------------------------------------------------

_TB_TEMPLATE = '''\
--------------------------------------------------------------------------------
-- Videomancer - FPGA-based video synthesis and processing firmware
-- File: tb_vit.vhd - VHDL Image Processing Testbench (auto-generated)
-- Copyright (C) 2026 LZX Industries LLC. All Rights Reserved.
--
-- DO NOT EDIT — generated by tools/vhdl_image_tester
--
-- Pipeline delay compensation:
--   Capture is aligned with the DUT's own output sync signals
--   (data_out.hsync_n / data_out.vsync_n).  Frame and line counters derived
--   from output sync edges determine the active-video region.  This matches
--   the real hardware video-encoder sampling and automatically accommodates
--   any pipeline depth.
--------------------------------------------------------------------------------

library ieee;
use ieee.std_logic_1164.all;
use ieee.numeric_std.all;

library std;
use std.textio.all;
use std.env.all;

library work;
use work.core_pkg.all;
use work.video_stream_pkg.all;
use work.video_timing_pkg.all;

-- ==========================================================================
-- Entity
-- ==========================================================================

entity tb_vit is
end entity tb_vit;

-- ==========================================================================
-- Architecture
-- ==========================================================================

architecture sim of tb_vit is

    -- -----------------------------------------------------------------------
    -- Constants
    -- -----------------------------------------------------------------------
    constant C_CLK_PERIOD      : time    := {clk_period_ns} ns;
    constant C_STIM_FILE       : string  := "{stim_file}";
    constant C_OUT_FILE        : string  := "{output_file}";

    -- Image dimensions (active-video region)
    constant C_IMG_WIDTH       : natural := {img_width};
    constant C_IMG_HEIGHT      : natural := {img_height};

    -- Number of output frames to skip before capturing
    constant C_WARMUP_FRAMES   : natural := {warmup_frames};

    -- Total expected output frames (warmup + capture)
    constant C_TOTAL_FRAMES    : natural := {total_frames};

    -- Active-video region offsets from sync edges (match stimulus generator)
    constant C_H_BLANK_HALF    : natural := {h_blank_half};
    constant C_V_BLANK_HALF    : natural := {v_blank_half};

    -- -----------------------------------------------------------------------
    -- DUT signals
    -- -----------------------------------------------------------------------
    signal clk          : std_logic := '0';
    signal registers_in : t_spi_ram := (others => (others => '0'));
    signal data_in      : t_video_stream_yuv444_30b := (
        y       => (others => '0'),
        u       => (others => '0'),
        v       => (others => '0'),
        avid    => '0',
        hsync_n => '1',
        vsync_n => '1',
        field_n => '1'
    );
    signal data_out : t_video_stream_yuv444_30b;

begin

    -- -----------------------------------------------------------------------
    -- Clock generation (free-running)
    -- -----------------------------------------------------------------------
    clk <= not clk after C_CLK_PERIOD / 2;

    -- -----------------------------------------------------------------------
    -- DUT instantiation
    -- -----------------------------------------------------------------------
    dut : entity work.program_top
        port map (
            clk          => clk,
            registers_in => registers_in,
            data_in      => data_in,
            data_out     => data_out
        );

    -- -----------------------------------------------------------------------
    -- Combined stimulus + capture process
    -- -----------------------------------------------------------------------
    p_sim : process
        -- File handles
        file   stim_f   : text;
        file   out_f    : text;

        -- Stimulus line parse variables
        variable l        : line;
        variable ol       : line;
        variable y_v      : integer;
        variable u_v      : integer;
        variable v_v      : integer;
        variable avid_v   : integer;
        variable hsync_v  : integer;
        variable vsync_v  : integer;
        variable field_v  : integer;
        variable cap_v    : integer;

        -- Output-sync capture state
        variable v_out_hsync_prev  : std_logic := '1';
        variable v_out_vsync_prev  : std_logic := '1';
        variable v_out_frame       : natural   := 0;
        variable v_capturing       : boolean   := false;
        variable v_line_in_frame   : natural   := 0;
        variable v_col_in_line     : natural   := 0;
        variable v_total_captured  : natural   := 0;

        -- Edge detection temporaries
        variable v_hsync_fall : boolean;
        variable v_vsync_fall : boolean;
    begin
        -- == Initialise registers ============================================
{reg_inits}

        -- == Open files ======================================================
        file_open(stim_f, C_STIM_FILE, read_mode);
        file_open(out_f,  C_OUT_FILE,  write_mode);

        -- == Main loop: drive stimulus, capture output =======================
        while not endfile(stim_f) loop
            readline(stim_f, l);
            read(l, y_v);
            read(l, u_v);
            read(l, v_v);
            read(l, avid_v);
            read(l, hsync_v);
            read(l, vsync_v);
            read(l, field_v);
            read(l, cap_v);           -- read but ignored; capture uses output sync

            -- Apply inputs (scheduled before next rising edge)
            data_in.y       <= std_logic_vector(to_unsigned(y_v, 10));
            data_in.u       <= std_logic_vector(to_unsigned(u_v, 10));
            data_in.v       <= std_logic_vector(to_unsigned(v_v, 10));
            data_in.avid    <= '1' when avid_v   = 1 else '0';
            data_in.hsync_n <= '1' when hsync_v  = 1 else '0';
            data_in.vsync_n <= '1' when vsync_v  = 1 else '0';
            data_in.field_n <= '1' when field_v  = 1 else '0';

            -- Clock edge — DUT samples inputs
            wait until rising_edge(clk);

            -- == Detect output sync edges ====================================
            v_hsync_fall := (v_out_hsync_prev = '1') and (data_out.hsync_n = '0');
            v_vsync_fall := (v_out_vsync_prev = '1') and (data_out.vsync_n = '0');
            v_out_hsync_prev := data_out.hsync_n;
            v_out_vsync_prev := data_out.vsync_n;

            -- == Frame counting ==============================================
            if v_vsync_fall then
                v_out_frame := v_out_frame + 1;
                v_line_in_frame := 0;
                v_col_in_line   := 0;
                report "VIT_FRAME: " & integer'image(v_out_frame)
                    & "/" & integer'image(C_TOTAL_FRAMES)
                    severity note;
                if v_out_frame > C_WARMUP_FRAMES and not v_capturing then
                    v_capturing := true;
                    report "VIT_CAPTURE: starting capture at output frame "
                        & integer'image(v_out_frame) severity note;
                end if;
            end if;

            -- == Line + column counting ======================================
            if v_hsync_fall then
                v_line_in_frame := v_line_in_frame + 1;
                v_col_in_line   := 0;
            else
                v_col_in_line := v_col_in_line + 1;
            end if;

            -- == Capture output ==============================================
            -- Capture when inside the active-video region of the capture frame
            -- and we have not yet collected a full frame of pixels.
            if v_capturing
                and v_total_captured < C_IMG_WIDTH * C_IMG_HEIGHT
                and v_line_in_frame >= C_V_BLANK_HALF + 1
                and v_line_in_frame <= C_V_BLANK_HALF + C_IMG_HEIGHT
                and v_col_in_line   >= C_H_BLANK_HALF
                and v_col_in_line   <  C_H_BLANK_HALF + C_IMG_WIDTH
            then
                write(ol, to_integer(unsigned(data_out.y)));
                write(ol, string'(" "));
                write(ol, to_integer(unsigned(data_out.u)));
                write(ol, string'(" "));
                write(ol, to_integer(unsigned(data_out.v)));
                writeline(out_f, ol);
                v_total_captured := v_total_captured + 1;
            end if;
        end loop;

        -- == Cleanup =========================================================
        file_close(stim_f);
        file_close(out_f);

        report "VIT_CAPTURED: " & integer'image(v_total_captured) & " pixels"
            severity note;

        -- Signal successful end of simulation
        report "VIT_DONE: simulation complete" severity note;
        std.env.stop(0);
        wait;
    end process p_sim;

end architecture sim;
'''


# ---------------------------------------------------------------------------
# Public API
# ---------------------------------------------------------------------------

def generate_testbench(
    output_path:     Path,
    stimulus_path:   Path,
    output_img_path: Path,
    register_values: list[int],
    img_width:       int,
    img_height:      int,
    clk_period_ns:   int = SIM_CLK_PERIOD_NS,
    warmup_frames:   int = SIM_WARMUP_FRAMES,
    h_blank_half:    int = 32,
    v_blank_half:    int = 4,
) -> None:
    """
    Generate tb_vit.vhd at *output_path*.

    Parameters
    ----------
    output_path      : Destination .vhd file path.
    stimulus_path    : Absolute path to the stimulus.txt file the testbench reads.
    output_img_path  : Absolute path where the testbench writes output.txt.
    register_values  : 32-element list of 10-bit register values (ABI v1.0).
    img_width        : Active-video image width in pixels.
    img_height       : Active-video image height in pixels.
    clk_period_ns    : Simulation clock period in nanoseconds (default 10 -> 100 MHz).
    warmup_frames    : Output frames to skip before capture begins (default 2).
    h_blank_half     : Pixel offset from hsync falling edge to first active column.
    v_blank_half     : Line offset from vsync falling edge to first active line.
    """
    reg_inits = _build_register_inits(register_values)

    total_frames = warmup_frames + 1  # warmup + capture frame

    content = _TB_TEMPLATE.format(
        clk_period_ns = clk_period_ns,
        stim_file     = str(stimulus_path).replace("\\", "/"),
        output_file   = str(output_img_path).replace("\\", "/"),
        reg_inits     = reg_inits,
        img_width     = img_width,
        img_height    = img_height,
        warmup_frames = warmup_frames,
        total_frames  = total_frames,
        h_blank_half  = h_blank_half,
        v_blank_half  = v_blank_half,
    )

    output_path.parent.mkdir(parents=True, exist_ok=True)
    output_path.write_text(content, encoding="utf-8")


def _build_register_inits(register_values: list[int]) -> str:
    """Produce VHDL signal-assignment lines for registers_in initialisation."""
    lines: list[str] = []
    for idx, val in enumerate(register_values[:ABI_SPI_RAM_SIZE]):
        val = max(0, min(1023, int(val)))
        bits = f"{val:010b}"
        lines.append(
            f'        registers_in({idx:2d}) <= "{bits}";'
            f'  -- 0x{idx:02X} = {val}'
        )
    return "\n".join(lines)
